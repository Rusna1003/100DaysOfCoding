public class Main {
	public static void main(String[] args) {
		
        for (int i = 1; i <= 10; i++) {
            if (i == 5) {
                System.out.println("Loop berhenti pada angka: " + i);
                break; // maka akan berhenti di angka 5
            }
            System.out.println("Angka: " + i);
        }
        
    }
}
	
